export const getLocalBotResponse = (questionText) => {
	// Normalize query to find closest match
	const cleanQuery = questionText.trim().toLowerCase();
	
	let bestMatch = null;
	let highestScore = 0;
	
	for (const question of questions) {
		const normQuestion = question.toLowerCase();
		// Compute basic word overlap
		const queryWords = cleanQuery.replace(/[?.,!]/g, "").split(/\s+/);
		const questionWords = normQuestion.replace(/[?.,!]/g, "").split(/\s+/);
		
		let matchCount = 0;
		for (const qw of queryWords) {
			if (qw && questionWords.includes(qw)) {
				matchCount++;
			}
		}
		
		const score = matchCount / Math.max(queryWords.length, 1);
		if (score > highestScore) {
			highestScore = score;
			bestMatch = question;
		}
	}
	
	// If matching score is low, return a polite default help message
	if (highestScore < 0.25) {
		return {
			question: questionText,
			answer: "Thank you for asking. KISS Agency is a full-service creative agency offering Web Design, custom Web/App Development, SEO optimization, and tailored AI Automation. If you have a specific project in mind, please fill out our Contact Form or call us at (515) 200-6679.",
			category: "agency"
		};
	}
	
	const matchedRes = responsesMap[bestMatch];
	return {
		question: bestMatch,
		answer: matchedRes.answer,
		category: matchedRes.category
	};
};

